package Com.Bridgelabz.Oops.Clinique;

public class Appointment
{
String doct_name;
String patient_name;
String Specialization;
String Timming;
int doctor_appointment;
public String getDoct_name() {
	return doct_name;
}
public void setDoct_name(String doct_name) {
	this.doct_name = doct_name;
}
public String getPatient_name() {
	return patient_name;
}
public void setPatient_name(String patient_name) {
	this.patient_name = patient_name;
}
public String getSpecialization() {
	return Specialization;
}
public void setSpecialization(String specialization) {
	Specialization = specialization;
}
public String getTimming() {
	return Timming;
}
public void setTimming(String timming) {
	Timming = timming;
}
public int getDoctor_appointment() {
	return doctor_appointment;
}
public void setDoctor_appointment(int doctor_appointment) {
	this.doctor_appointment = doctor_appointment;
}


} 
