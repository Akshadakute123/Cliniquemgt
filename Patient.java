package Com.Bridgelabz.Oops.Clinique;

public class Patient 
{
 String patient_name;
 int patient_id;
 long patient_Mobileno;
 int patient_Age;
public String getPatient_name() {
	return patient_name;
}
public void setPatient_name(String patient_name) {
	this.patient_name = patient_name;
}
public int getPatient_id() {
	return patient_id;
}
public void setPatient_id(int patient_id) {
	this.patient_id = patient_id;
}
public long getPatient_Mobileno() {
	return patient_Mobileno;
}
public void setPatient_Mobileno(long patient_Mobileno) {
	this.patient_Mobileno = patient_Mobileno;
}
public int getPatient_Age() {
	return patient_Age;
}
public void setPatient_Age(int patient_Age) {
	this.patient_Age = patient_Age;
}
 
 
}
